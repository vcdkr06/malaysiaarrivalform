// Get all islands with atoll code for dropdown - sorted by atoll code then island name
export const getIslandsWithAtoll = (): { value: string; label: string; atoll: string }[] => {
  return maldivesIslands.map(island => ({
    value: `${island.atoll}-${island.name}`,
    label: `${island.atoll} - ${island.name}`,
    atoll: island.atoll
  })).sort((a, b) => {
    // Sort by atoll first, then by island name
    const atollCompare = a.atoll.localeCompare(b.atoll);
    if (atollCompare !== 0) return atollCompare;
    return a.label.localeCompare(b.label);
  });
};

// Get unique island names for dropdown (legacy)
export const getIslandNames = (): string[] => {
  const uniqueNames = [...new Set(maldivesIslands.map(island => island.name))];
  return uniqueNames.sort();
};
